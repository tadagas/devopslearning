package JavaBasics;

public class ConstructorA {
	
	
	public ConstructorA()
	{
		System.out.println("Default class");
		
	}
	public ConstructorA(int i)
	{
		System.out.println("Simple parent class with I");
		
	}
	
	
	public ConstructorA(int a, int j)
	{
		System.out.println("Simple parent class with value of a= "+a);
		System.out.println("Simple parent class with value of j= "+j);
	}
}
